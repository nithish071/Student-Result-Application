    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String roll = jTextField1.getText();
        String physics = jTextField2.getText();
        String maths = jTextField3.getText();
        String dbms = jTextField4.getText();
        String os = jTextField5.getText();
        try{
            // instatiate the jdbc
            Class.forName("com.mysql.cj.jdbc.Driver");
            // create the conncection using jdbc Drivermanager
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Nithish@99");
            // create statement using connection
            Statement st = con.createStatement();
            //sql query to get the student with given roll number if not present tell the user that user is not registered
            ResultSet rs = st.executeQuery("select * from student where rollno='"+roll+"'");
            if(rs.next()){
               // if user found update the marks 
               st.executeUpdate("insert into result(rollno,physics,maths,dbms,os) values('"+roll+"','"+physics+"','"+maths+"','"+dbms+"','"+os+"')");
               JOptionPane.showMessageDialog(null, "Marks saved sucessfully");
               setVisible(false);
               // reloading the same page so that new set of data can be entered
               new addResult().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Roll number is not registered");
            }
        } catch (Exception e){
            // throws any execption occurs
            JOptionPane.showMessageDialog(null, e.toString());
        }
                
    }//GEN-LAST:event_jButton6ActionPerformed

