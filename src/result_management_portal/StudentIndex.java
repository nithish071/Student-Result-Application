    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField13.getText();
        try{
            // instatiate the jdbc
            Class.forName("com.mysql.cj.jdbc.Driver");
            // create the conncection using jdbc Drivermanager
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Nithish@99");
            // create statement using connection
            Statement st = con.createStatement();
            // inner join both table so that we can display values from both tables
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno = '"+rollno+"' and result.rollno = '"+rollno+"'");
            if(rs.next()){
                
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField6.setText(rs.getString(6));
                jTextField5.setText(rs.getString(5));
                jTextField11.setText(rs.getString(8));
                jTextField10.setText(rs.getString(9));
                jTextField12.setText(rs.getString(10));
                jTextField14.setText(rs.getString(11));
                
                int p = Integer.parseInt(rs.getString(8));
                int m = Integer.parseInt(rs.getString(9));
                int d = Integer.parseInt(rs.getString(10));
                int o = Integer.parseInt(rs.getString(11));
                jTextField1.setText(String.valueOf(p+m+d+o));
            }
        } catch (Exception e){
            // throws any execption occurs
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

